create policy "usuarios_select_autenticados" on public.usuarios
for select to authenticated using (public.is_admin() or id = auth.uid());
create policy "usuarios_admin_all" on public.usuarios
for all to authenticated using (public.is_admin()) with check (public.is_admin());

create policy "areas_select_todos" on public.areas_conhecimento
for select to authenticated using (true);
create policy "areas_admin_all" on public.areas_conhecimento
for all to authenticated using (public.is_admin()) with check (public.is_admin());

create policy "projetos_select_todos" on public.projetos
for select to authenticated using (true);
create policy "projetos_admin_all" on public.projetos
for all to authenticated using (public.is_admin()) with check (public.is_admin());
create policy "projetos_professor_insert" on public.projetos
for insert to authenticated with check (public.get_tipo_usuario() in ('Professor', 'Administrador'));
create policy "projetos_professor_update" on public.projetos
for update to authenticated using (public.get_tipo_usuario() in ('Professor', 'Administrador'))
with check (public.get_tipo_usuario() in ('Professor', 'Administrador'));

