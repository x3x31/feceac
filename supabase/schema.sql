for all to authenticated using (
  exists (
    select 1 from public.avaliacoes a
    where a.id = avaliacao_id and a.avaliador_id = auth.uid()
  )
) with check (
  exists (
    select 1 from public.avaliacoes a
    where a.id = avaliacao_id and a.avaliador_id = auth.uid()
  )
);

