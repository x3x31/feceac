-- 2. Tabelas

CREATE TABLE public.usuarios (
  id uuid PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  nome text NOT NULL,
  email text NOT NULL UNIQUE,
  tipo text NOT NULL CHECK (tipo IN ('Administrador', 'Professor', 'Avaliador', 'Aluno')),
  ativo boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE public.tipos_projeto (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome text NOT NULL UNIQUE
);

CREATE TABLE public.areas_conhecimento (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome text NOT NULL UNIQUE,
  tipo_projeto_id bigint REFERENCES public.tipos_projeto(id)
);

CREATE TABLE public.professores (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  matricula text UNIQUE NOT NULL,
  nome text NOT NULL
);

CREATE TABLE public.alunos (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome text NOT NULL,
  matricula text UNIQUE,
  turma text,
  turno text
);

CREATE TABLE public.projetos (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ano integer NOT NULL,
  codigo text NOT NULL UNIQUE,
  titulo text NOT NULL,
  area_id bigint REFERENCES public.areas_conhecimento(id),
  tipo_projeto_id bigint REFERENCES public.tipos_projeto(id),
  orientador_id bigint NOT NULL,
  coorientador_id bigint,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT projetos_orientador_id_fkey FOREIGN KEY (orientador_id) REFERENCES public.professores(id),
  CONSTRAINT projetos_coorientador_id_fkey FOREIGN KEY (coorientador_id) REFERENCES public.professores(id)
);

CREATE TABLE public.projeto_alunos (
  projeto_id bigint REFERENCES public.projetos(id) ON DELETE CASCADE,
  aluno_id bigint REFERENCES public.alunos(id) ON DELETE CASCADE,
  turma text,
  PRIMARY KEY (projeto_id, aluno_id)
);

CREATE TABLE public.criterios (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  descricao text NOT NULL,
  peso numeric(5,2) NOT NULL DEFAULT 1 CHECK (peso > 0),
  observacoes text,
  tipo_projeto_id bigint REFERENCES public.tipos_projeto(id)
);

CREATE TABLE public.avaliacoes (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  projeto_id bigint NOT NULL REFERENCES public.projetos(id) ON DELETE CASCADE,
  avaliador_id uuid NOT NULL REFERENCES public.usuarios(id),
  data date NOT NULL DEFAULT current_date,
  unique (projeto_id, avaliador_id)
);

CREATE TABLE public.notas (
  avaliacao_id bigint REFERENCES public.avaliacoes(id) ON DELETE CASCADE,
  criterio_id bigint REFERENCES public.criterios(id) ON DELETE CASCADE,
  nota numeric(4,2) NOT NULL CHECK (nota >= 0 AND nota <= 10),
  PRIMARY KEY (avaliacao_id, criterio_id)
);

-- 3. Funcoes auxiliares

CREATE OR REPLACE FUNCTION public.get_tipo_usuario()
RETURNS text
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT tipo FROM public.usuarios WHERE id = auth.uid() AND ativo = true;
$$;

CREATE OR REPLACE FUNCTION public.is_admin()
RETURNS boolean
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT coalesce(public.get_tipo_usuario() = 'Administrador', false);
$$;

-- Trigger: ao excluir de usuarios, exclui tambem de auth.users
CREATE OR REPLACE FUNCTION public.handle_usuario_delete()
RETURNS TRIGGER AS $$
BEGIN
  DELETE FROM auth.users WHERE id = OLD.id;
  RETURN OLD;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE TRIGGER on_usuario_deleted
  AFTER DELETE ON public.usuarios
  FOR EACH ROW
  EXECUTE FUNCTION public.handle_usuario_delete();

