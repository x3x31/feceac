import { readFileSync, writeFileSync } from 'fs';

const TEMP = 'D:\\Projetos\\feceac\\temp\\';

const normalize = (s) => (s || '').trim().toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '').replace(/[^a-z0-9\s]/g, '').replace(/\s+/g, ' ').trim();
const dedupLetters = (s) => s.replace(/(.)\1+/g, '$1');

const nameMatch = (a, b) => {
  const na = dedupLetters(normalize(a));
  const nb = dedupLetters(normalize(b));
  if (na === nb) return 1;
  if (na.includes(nb) || nb.includes(na)) return 0.9;
  const aParts = na.split(' ');
  const bParts = nb.split(' ');
  let matches = 0;
  for (const ap of aParts) {
    if (ap.length < 3) { matches++; continue; }
    for (const bp of bParts) {
      if (dedupLetters(ap).includes(dedupLetters(bp)) || dedupLetters(bp).includes(dedupLetters(ap))) { matches++; break; }
    }
  }
  const shortLen = Math.min(aParts.length, bParts.length);
  if (matches >= shortLen && shortLen >= 2) return 0.85;
  const ratio = matches / Math.max(aParts.length, bParts.length);
  return ratio >= 0.6 ? ratio : 0;
};

const profAliases = {
  'mansinho': 'francisco de assis verissimo junior',
  'michael alves': 'michel wiillian alves',
  'carla cardoso': 'karla raphaela cardoso',
  'carla': 'karla raphaela cardoso',
};

const findProf = (name, profs) => {
  if (!name) return null;
  const n = normalize(name);
  const aliasTarget = profAliases[n];
  if (aliasTarget) {
    for (const p of profs) {
      if (normalize(p.nome) === aliasTarget) return p;
    }
  }
  let best = null, bestScore = 0;
  for (const p of profs) {
    const score = nameMatch(name, p.nome);
    if (score > bestScore) { bestScore = score; best = p; }
  }
  return bestScore >= 0.6 ? best : null;
};

const findAluno = (name, alunos) => {
  if (!name) return null;
  let best = null, bestScore = 0;
  for (const a of alunos) {
    const score = nameMatch(name, a.nome);
    if (score > bestScore) { bestScore = score; best = a; }
  }
  return bestScore >= 0.6 ? best : null;
};

const areaMap = {
  'linguagens e suas tecnologias': 3,
  'exatas e engenharia': 2,
  'ciencias biologicas e da saude': 1,
  'ciencias humanas e sociais aplicadas': 4,
};

const findAreaId = (name) => {
  if (!name) return null;
  return areaMap[normalize(name)] || null;
};

// ============================================================
// 1. Ler fontes
// ============================================================
const profLines = readFileSync(TEMP + 'lista_professor.txt', 'utf8')
  .split('\n').filter(l => l.trim() && !l.startsWith('MATRICULA'));
const alunosLines = readFileSync(TEMP + 'lista_alunos.txt', 'utf8')
  .split('\n').filter(l => l.trim() && !l.startsWith('MATR'));
const projetosTxt = readFileSync(TEMP + 'projetos.txt', 'utf8');

// ============================================================
// 2. Parsear professores (deduplicar por matrícula)
// ============================================================
const profs = [];
const seenProfMat = new Set();
for (const line of profLines) {
  const [matricula, ...nomeParts] = line.split(';');
  const nome = nomeParts.join(';').trim();
  if (!matricula || !nome) continue;
  const mat = matricula.trim();
  if (seenProfMat.has(mat)) continue;
  seenProfMat.add(mat);
  profs.push({ id: profs.length + 1, matricula: mat, nome });
}
console.log(`Professores: ${profs.length} (deduplicados de ${profLines.length} linhas)`);

// ============================================================
// 3. Gerar professores.sql
// ============================================================
let profSql = '-- ============================================================\n';
profSql += '-- Criacao da tabela professores e importacao de dados\n';
profSql += `-- Fonte: temp/lista_professor.txt (${profs.length} registros)\n`;
profSql += '-- ============================================================\n\n';
profSql += 'CREATE TABLE IF NOT EXISTS public.professores (\n';
profSql += '  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,\n';
profSql += '  matricula text UNIQUE NOT NULL,\n';
profSql += '  nome text NOT NULL\n';
profSql += ');\n\n';
profSql += 'TRUNCATE TABLE public.professores RESTART IDENTITY CASCADE;\n\n';
profSql += 'INSERT INTO public.professores (id, matricula, nome) VALUES\n';
profSql += profs.map(p => `  (${p.id}, '${p.matricula}', '${p.nome.replace(/'/g, "''")}')`).join(',\n');
profSql += '\nON CONFLICT (matricula) DO NOTHING;\n';
writeFileSync(TEMP + 'professores.sql', profSql);

// ============================================================
// 4. Parsear alunos
// ============================================================
const alunos = [];
const seenAluMat = new Set();
for (const line of alunosLines) {
  const parts = line.split(';').map(s => s.trim());
  if (parts.length < 4) continue;
  const [matricula, turma, turno, nome] = parts;
  if (!matricula || !nome) continue;
  if (seenAluMat.has(matricula)) continue;
  seenAluMat.add(matricula);
  alunos.push({ id: alunos.length + 1, matricula, turma, turno, nome });
}
console.log(`Alunos: ${alunos.length} (deduplicados de ${alunosLines.length} linhas)`);

// ============================================================
// 5. Gerar alunos.sql
// ============================================================
let aluSql = '-- ============================================================\n';
aluSql += '-- Ajuste da tabela alunos e importacao de dados\n';
aluSql += `-- Fonte: temp/lista_alunos.txt (${alunos.length} registros)\n`;
aluSql += '-- ============================================================\n\n';
aluSql += 'ALTER TABLE public.alunos ADD COLUMN IF NOT EXISTS matricula text UNIQUE;\n';
aluSql += 'ALTER TABLE public.alunos ADD COLUMN IF NOT EXISTS turma text;\n';
aluSql += 'ALTER TABLE public.alunos ADD COLUMN IF NOT EXISTS turno text;\n\n';
aluSql += 'TRUNCATE TABLE public.projeto_alunos RESTART IDENTITY CASCADE;\n';
aluSql += 'TRUNCATE TABLE public.alunos RESTART IDENTITY CASCADE;\n\n';
aluSql += 'INSERT INTO public.alunos (id, matricula, turma, turno, nome) VALUES\n';
aluSql += alunos.map(a => `  (${a.id}, '${a.matricula}', '${a.turma}', '${a.turno}', '${a.nome.replace(/'/g, "''")}')`).join(',\n');
aluSql += '\nON CONFLICT (matricula) DO NOTHING;\n';
writeFileSync(TEMP + 'alunos.sql', aluSql);

// ============================================================
// 6. Parsear projetos
// ============================================================
const projLines = projetosTxt.split('\n').filter(l => l.trim() && !l.startsWith('TÍTULO'));
const projetos = [];
const projetoAlunos = [];
const unmatched = [];
const unmatchedStructured = [];

let projetoId = 1;

for (const line of projLines) {
  const parts = line.split(';').map(s => s.trim());
  if (parts.length < 5) continue;

  const titulo = parts[0];
  const orientadorNome = parts[1];
  const turno = parts[2];
  const areaNome = parts[3];
  const alunoNomes = [parts[4], parts[5], parts[6], parts[7]].filter(Boolean);
  const coorientadorNome = parts[8] || null;

  const orientador = findProf(orientadorNome, profs);
  const coorientador = coorientadorNome ? findProf(coorientadorNome, profs) : null;
  const areaId = findAreaId(areaNome);

  if (!orientador) {
    unmatched.push(`ORIENTADOR: "${orientadorNome}" (projeto: ${titulo})`);
    unmatchedStructured.push({ tipo: 'Professor (Orientador)', nome: orientadorNome, projeto: titulo, turno: null });
  }
  if (coorientadorNome && !coorientador) {
    unmatched.push(`CO-ORIENTADOR: "${coorientadorNome}" (projeto: ${titulo})`);
    unmatchedStructured.push({ tipo: 'Professor (Coorientador)', nome: coorientadorNome, projeto: titulo, turno: null });
  }
  if (!areaId) unmatched.push(`AREA: "${areaNome}" (projeto: ${titulo})`);

  projetos.push({
    id: projetoId,
    titulo,
    area_id: areaId,
    orientador_id: orientador ? orientador.id : null,
    coorientador_id: coorientador ? coorientador.id : null,
  });

  for (const alunoNome of alunoNomes) {
    const aluno = findAluno(alunoNome, alunos);
    if (!aluno) {
      unmatched.push(`ALUNO: "${alunoNome}" (projeto: ${titulo})`);
      unmatchedStructured.push({ tipo: 'Aluno(a)', nome: alunoNome, projeto: titulo, turno });
      continue;
    }
    projetoAlunos.push({
      projeto_id: projetoId,
      aluno_id: aluno.id,
      turma: aluno.turma,
    });
  }

  projetoId++;
}

// ============================================================
// 7. Gerar projetos.sql
// ============================================================
let sql = '-- ============================================================\n';
sql += '-- FECEAC - Insert de projetos e projeto_alunos\n';
sql += '-- Gerado automaticamente a partir de projetos.txt\n';
sql += '-- ============================================================\n\n';

sql += 'ALTER TABLE public.projetos ALTER COLUMN orientador_id DROP NOT NULL;\n\n';

sql += 'DELETE FROM public.projeto_alunos;\n';
sql += 'DELETE FROM public.projetos;\n\n';
sql += 'SELECT setval(pg_get_serial_sequence(\'public.projetos\', \'id\'), 1, false);\n\n';

sql += '-- PROJETOS\n';
sql += 'INSERT INTO public.projetos (id, ano, titulo, area_id, tipo_projeto_id, orientador_id, coorientador_id) VALUES\n';

const projetoLines = projetos.map(p => {
  const titulo = p.titulo.replace(/'/g, "''");
  const coo = p.coorientador_id || 'NULL';
  return `  (${p.id}, 2025, '${titulo}', ${p.area_id}, 1, ${p.orientador_id}, ${coo})`;
});
sql += projetoLines.join(',\n') + ';\n\n';
sql += `SELECT setval(pg_get_serial_sequence('public.projetos', 'id'), ${projetos.length});\n\n`;

sql += '-- PROJETO_ALUNOS\n';
sql += 'INSERT INTO public.projeto_alunos (projeto_id, aluno_id, turma) VALUES\n';
const paLines = projetoAlunos.map(pa =>
  `  (${pa.projeto_id}, ${pa.aluno_id}, '${pa.turma}')`
);
sql += paLines.join(',\n') + ';\n';

if (unmatched.length) {
  sql += '\n-- ============================================================\n';
  sql += '-- ATENÇÃO: Itens não encontrados no banco\n';
  sql += '-- ============================================================\n';
  for (const u of unmatched) {
    sql += `-- ${u}\n`;
  }
}

writeFileSync(TEMP + 'projetos.sql', sql);

// ============================================================
// 8. Gerar nao_encontrados.txt
// ============================================================
let report = '============================================================\n';
report += 'FECEAC - Itens não encontrados no banco de dados\n';
report += '============================================================\n\n';

const profsUnmatched = unmatchedStructured.filter(u => u.tipo.startsWith('Professor'));
const alunosUnmatched = unmatchedStructured.filter(u => u.tipo.startsWith('Aluno'));

report += `TOTAL: ${unmatchedStructured.length} itens não encontrados\n`;
report += `  Professores: ${profsUnmatched.length}\n`;
report += `  Alunos: ${alunosUnmatched.length}\n\n`;

if (profsUnmatched.length) {
  report += '------------------------------------------------------------\n';
  report += 'PROFESSORES NÃO ENCONTRADOS\n';
  report += '------------------------------------------------------------\n';
  for (const u of profsUnmatched) {
    report += `${u.tipo}: ${u.nome}\n`;
    report += `  Projeto: ${u.projeto}\n\n`;
  }
}

if (alunosUnmatched.length) {
  report += '------------------------------------------------------------\n';
  report += 'ALUNOS NÃO ENCONTRADOS\n';
  report += '------------------------------------------------------------\n';
  for (const u of alunosUnmatched) {
    report += `${u.nome}\n`;
    report += `  Projeto: ${u.projeto}\n`;
    report += `  Turno: ${u.turno || 'N/A'}\n\n`;
  }
}

writeFileSync(TEMP + 'nao_encontrados.txt', report);

// ============================================================
// 9. Resumo
// ============================================================
console.log(`\nArquivos gerados:`);
console.log(`  professores.sql  (${profs.length} registros)`);
console.log(`  alunos.sql       (${alunos.length} registros)`);
console.log(`  projetos.sql     (${projetos.length} projetos, ${projetoAlunos.length} vinculações)`);
console.log(`  nao_encontrados.txt (${unmatchedStructured.length} não encontrados)`);
if (unmatched.length) {
  console.log('\nNão encontrados:');
  unmatched.forEach(u => console.log(`  - ${u}`));
}
